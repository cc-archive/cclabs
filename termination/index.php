<h3 id="how_can_it_assist">How can this tool assist you?</h3>

<p>Are you the creator of a book, a cartoon, a screenplay, a movie, artwork, or some other
type of creative <a class="glossarylink" href="glossary.html#work">work</a> that is protected by
<a class="glossarylink" href="glossary.html#copyright_law">copyright law</a>?<br>
<br>
Are you a <a class="glossarylink" href="glossary.html#descendants">spouse, widow, other relative or authorized
representative</a> of an <a class="glossarylink" href="glossary.html#author">author or artist</a>?<br>
<br>
Did you or the <a class="glossarylink" href="glossary.html#author">author or artist</a> enter into
an <a class="glossarylink" href="glossary.html#agreement">agreement or transfer</a> in relation to that work with
someone else who is now reaping commercial benefits from your or the author or artist's
hard work and you would now like to share in the benefits?</p>

<p>If so, this tool may assist you to terminate that agreement or transfer.  Terminating
the agreement means that the rights in the work return to you, and you are again in control
of all of the rights in relation to your work.</p>

<p>To learn more about this tool and whether it might be useful for you, feel free to check out
