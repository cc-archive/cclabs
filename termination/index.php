<h3 id="what_does_it_mean">What does this mean for you?</h3>

<p>Under US law, <a class="glossarylink" href="glossary.html#author">authors and artists</a> and, in some cases, their
<a class="glossarylink" href="glossary.html#descendants">spouse, widow, other relatives or authorized representatives</a>
have the chance to terminate earlier <a class="glossarylink" href="glossary.html#agreement">agreements or transfers</a>
that they may have entered into many years ago before they
knew how successful or profitable their work would be.  Under certain provisions of the
US Copyright Act, commonly called the "termination of transfer" provisions,
authors and artists, and in some cases,
their spouse, widow, other relatives or authorized representatives
can terminate these agreements and reclaim their US rights.
They can then negotiate new licensing and other deals with the strength of the knowledge of
market value of their work.</p>

<h3 id="what_does_it_do">What does this tool do?</h3>

<p>This tool is designed to make it easier for you to exercise your rights to terminate
<a class="glossarylink" href="glossary.html#agreement">agreements and transfers</a> that you or an
<a class="glossarylink" href="glossary.html#author">author or artist</a> entered into
before being able to assess the true market for the work.  Specifically, this tool
can assist you in identifying whether you may have a right to terminate an agreement
or transfer and if so, it calculates, based on the information you provide, whether
you may be to activate your termination right at the moment.  In addition, this tool
gathers some basic details about the creation and exploitation
of the work that can be passed to either a lawyer or a
<a class="glossarylink" href="glossary.html#clinic">participating law clinic</a>
that can then represent you in effecting your termination rights.</p>

<p>To learn more about the information that you will need in order to complete the tool,
<a href="what_information_do_i_need.php">click here</a>.</p>

